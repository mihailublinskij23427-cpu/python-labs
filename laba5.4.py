import numpy as np

# Принимаем радиус Земли в километрах
R_earth = 6371.0  # км

# Данные для всех 5 ИСЗ (включая эллипсоидные и остальные)
all_orbits = [
    (6, 0.18, 'Эллипсоидная 1'),
    (6, 0.33, 'Эллипсоидная 2'),
    (6, 0.55, 'Эллипсоидная 3'),
    (6, 0.67, 'Эллипсоидная 4'),
    (6, 0.89, 'Эллипсоидная 5'),
    (3, 0, 'Круговая'),
    (2, 1, 'Параболическая'),
    (2, 1.7, 'Гиперболическая 1'),
    (1, 2.3, 'Гиперболическая 2'),
    (4, 0, 'Круговая 2')  # Добавлено для полноты
]

print("Расчет высот полета ИСЗ:")
print("-" * 50)

for p, e, label in all_orbits:
    if e == 1:  # Параболическая орбита
        print(f"{label}: Параболическая орбита. Большая полуось a не определена.")
        continue
    elif e > 1:  # Гиперболическая орбита
        # Для гиперболы a отрицательно, но мы можем рассчитать r_p
        a = p / (1 - e**2)  # a будет отрицательным
        r_p = a * (1 - e)   # r_p будет положительным
        h_p = r_p - R_earth
        print(f"{label}: Высота в перигее h_p = {h_p:.2f} км")
    else:  # Эллиптическая или круговая орбита
        a = p / (1 - e**2)
        r_p = a * (1 - e)
        r_a = a * (1 + e)
        h_p = r_p - R_earth
        h_a = r_a - R_earth
        print(f"{label}: Высота в перигее h_p = {h_p:.2f} км, высота в апогее h_a = {h_a:.2f} км")