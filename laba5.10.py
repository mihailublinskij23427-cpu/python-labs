import numpy as np

# Принимаем радиус Земли в километрах
R_earth = 6371.0  # км

# Параметры для "эллипсоидных" ИСЗ
p_values = [6, 6, 6, 6, 6]      # Параметр орбиты (в тыс. км)
e_values = [0.18, 0.33, 0.55, 0.67, 0.89]  # Эксцентриситеты
labels = [
    'Эллипсоидная 1',
    'Эллипсоидная 2',
    'Эллипсоидная 3',
    'Эллипсоидная 4',
    'Эллипсоидная 5'
]

print("Расчет высот полета ИСЗ (эллиптические орбиты):")
print("-" * 70)

for i, (p, e, label) in enumerate(zip(p_values, e_values, labels)):
    # Переводим p в км (т.к. p дано в тыс. км)
    p_km = p * 1000
    
    # Рассчитываем большую полуось a
    a = p_km / (1 - e**2)
    
    # Рассчитываем радиусы перигея и апогея
    r_p = a * (1 - e)   # перигей
    r_a = a * (1 + e)   # апогей
    
    # Рассчитываем высоты над поверхностью Земли
    h_p = r_p - R_earth
    h_a = r_a - R_earth
    
    print(f"{label}: Высота в перигее h_p = {h_p:.2f} км, высота в апогее h_a = {h_a:.2f} км")